// Condition
let num = 10;
// Simple if-else
if (num == 0) {
  console.log("zero")
} else if(num % 2 == 0) {
  console.log("even")
} else {
  console.log("odd")
}

// switch
let cond = true
switch(cond) {
  case true:
    console.log("true");
    break;
  default:
    console.log("false");
    break;
}

